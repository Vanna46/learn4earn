package testing_basics;

import org.testng.annotations.Test;

public class Assertion_Concept {
	
	@Test
	public static void a() {
		System.out.println("This is a Primary Test Case");
	}
	
	public static void b() {
		System.out.println("This is a Secondary Test Case");
	}
	
	public static void c() {
		System.out.println("This is a Tertiary Test Case");
	}
	
	public static void d() {
		System.out.println("This is a quartly Test Case");
	}

}
